double random_d(double low, double high)
{
    double r;
    static unsigned int seed;

    seed = rand() % 9828394823;
    seed %= 12098838295;
    srand(seed);

    r = rand() % (int)(high-low);
    r += low;
    //printf("random_d(): r = %lf\n",r);
    return r;
}

int random_i(int low, int high)
{
    return ((rand()/RAND_MAX)*(high-low)+low);
}

int round_d(double x)
{
    if((x-(int)x)>0.5)
        return (int)x + 1;
    else
        return (int)x;
}

